void db_save_setting(PGconn* conn, const char* key, const char* value) {
    const char* values[2];
    PGresult* result;

    if (!conn || !key || !value) return;

    values[0] = key;
    values[1] = value;
    result = exec_prepared_stmt(
        conn,
        "db_save_setting",
        "INSERT INTO app_settings (key, value, updated_at) "
        "VALUES ($1, $2, NOW()) "
        "ON CONFLICT (key) DO UPDATE SET value = EXCLUDED.value, updated_at = EXCLUDED.updated_at",
        2,
        values);
    if (result) PQclear(result);
}

char* db_get_setting(PGconn* conn, const char* key) {
    const char* values[1];
    PGresult* result;
    char* output;

    if (!conn || !key) {
        output = malloc(1);
        if (output) output[0] = '\0';
        return output;
    }

    values[0] = key;
    result = exec_prepared_stmt(
        conn,
        "db_get_setting",
        "SELECT COALESCE(value, '') FROM app_settings WHERE key = $1",
        1,
        values);
    if (!result || PQresultStatus(result) != PGRES_TUPLES_OK || PQntuples(result) == 0) {
        if (result) PQclear(result);
        output = malloc(1);
        if (output) output[0] = '\0';
        return output;
    }

    output = malloc(256);
    if (output) {
        copy_text(output, 256, result_value(result, 0, 0));
    }
    PQclear(result);
    return output;
}

int db_save_backup_record(PGconn* conn, double size_kb, const char* provider,
                         const char* url, const char* status, const char* notes,
                         int created_by, int is_local, const char* local_path) {
    char size_buf[32];
    char created_by_buf[16];
    char is_local_buf[8];
    const char* values[8];
    PGresult* result;
    int last_id = -1;

    if (!conn) return -1;

    snprintf(size_buf, sizeof(size_buf), "%.2f", size_kb);
    snprintf(created_by_buf, sizeof(created_by_buf), "%d", created_by);
    snprintf(is_local_buf, sizeof(is_local_buf), "%d", is_local);

    values[0] = size_buf;
    values[1] = provider ? provider : "local";
    values[2] = url ? url : "";
    values[3] = status ? status : "OK";
    values[4] = notes ? notes : "";
    values[5] = created_by > 0 ? created_by_buf : NULL;
    values[6] = is_local_buf;
    values[7] = local_path ? local_path : "";

    result = exec_prepared_stmt(
        conn,
        "db_save_backup_record",
        "INSERT INTO backup_history (size_kb, provider, url, status, notes, created_by, is_local, local_path) "
        "VALUES ($1::double precision, $2, $3, $4, $5, $6::int, $7::int, $8) RETURNING id",
        8,
        values);
    if (result && PQresultStatus(result) == PGRES_TUPLES_OK && PQntuples(result) == 1) {
        last_id = result_int(result, 0, 0);
    }
    if (result) PQclear(result);
    return last_id;
}
