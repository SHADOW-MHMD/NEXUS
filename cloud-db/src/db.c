static void copy_text(char* dest, size_t dest_size, const char* src) {
    if (!dest || dest_size == 0) return;
    if (!src) src = "";
    snprintf(dest, dest_size, "%s", src);
}

static const char* normalize_item_condition(const char* condition) {
    if (!condition || condition[0] == '\0') return "Working";
    if (strcasecmp(condition, "Working") == 0) return "Working";
    if (strcasecmp(condition, "Damaged") == 0) return "Damaged";
    if (strcasecmp(condition, "Under Repair") == 0) return "Under Repair";
    return "Working";
}

static int exec_simple(PGconn* conn, const char* sql) {
    PGresult* result = PQexec(conn, sql);
    ExecStatusType status = PQresultStatus(result);
    if (status != PGRES_COMMAND_OK && status != PGRES_TUPLES_OK) {
        fprintf(stderr, "[db] SQL error: %s\n", PQerrorMessage(conn));
        PQclear(result);
        return 0;
    }
    PQclear(result);
    return 1;
}

static int is_prepared(const char* name) {
    size_t i;
    for (i = 0; i < prepared_statement_count; i++) {
        if (strcmp(prepared_statements[i], name) == 0) {
            return 1;
        }
    }
    return 0;
}

static int mark_prepared(const char* name) {
    if (prepared_statement_count >= MAX_PREPARED_STATEMENTS) {
        fprintf(stderr, "[db] Prepared statement registry exhausted\n");
        return 0;
    }
    snprintf(prepared_statements[prepared_statement_count],
             sizeof(prepared_statements[prepared_statement_count]), "%s", name);
    prepared_statement_count++;
    return 1;
}

static int ensure_prepared(PGconn* conn, const char* name, const char* sql, int n_params) {
    PGresult* result;

    if (is_prepared(name)) {
        return 1;
    }

    result = PQprepare(conn, name, sql, n_params, NULL);
    if (PQresultStatus(result) != PGRES_COMMAND_OK) {
        fprintf(stderr, "[db] Failed to prepare %s: %s\n", name, PQerrorMessage(conn));
        PQclear(result);
        return 0;
    }

    PQclear(result);
    return mark_prepared(name);
}

static PGresult* exec_prepared_stmt(PGconn* conn, const char* name, const char* sql,
                                    int n_params, const char* const* values) {
    if (!ensure_prepared(conn, name, sql, n_params)) {
        return NULL;
    }
    return PQexecPrepared(conn, name, n_params, values, NULL, NULL, 0);
}

static const char* result_value(PGresult* result, int row, int column) {
    if (!result || PQgetisnull(result, row, column)) {
        return "";
    }
    return PQgetvalue(result, row, column);
}

static int result_int(PGresult* result, int row, int column) {
    return atoi(result_value(result, row, column));
}

static double result_double(PGresult* result, int row, int column) {
    return atof(result_value(result, row, column));
}

