static time_t parse_timestamp(const char* value) {
    struct tm tm_value;
    char* parsed = NULL;

    if (!value || value[0] == '\0') {
        return (time_t)-1;
    }

    memset(&tm_value, 0, sizeof(tm_value));
    parsed = strptime(value, "%Y-%m-%d %H:%M:%S", &tm_value);
    if (!parsed) {
        memset(&tm_value, 0, sizeof(tm_value));
        parsed = strptime(value, "%Y-%m-%d", &tm_value);
    }

    if (!parsed) {
        return (time_t)-1;
    }

    tm_value.tm_isdst = -1;
    return mktime(&tm_value);
}

static void compute_issue_status(IssueRecord* issue) {
    time_t now_value;
    time_t issue_time;

    if (!issue) return;
    if (strcmp(issue->status, "RETURNED") == 0 || issue->return_date[0] != '\0') return;

    issue_time = parse_timestamp(issue->issue_date);
    if (issue_time == (time_t)-1) return;

    now_value = time(NULL);
    if (difftime(now_value, issue_time) > (14 * 24 * 60 * 60)) {
        copy_text(issue->status, sizeof(issue->status), "OVERDUE");
    }
}

