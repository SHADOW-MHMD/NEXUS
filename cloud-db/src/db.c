static int column_exists(PGconn* conn, const char* table, const char* column) {
    const char* sql =
        "SELECT 1 "
        "FROM information_schema.columns "
        "WHERE table_schema = 'public' AND table_name = $1 AND column_name = $2";
    const char* values[2] = {table, column};
    PGresult* result = exec_prepared_stmt(conn, "column_exists", sql, 2, values);
    int found = 0;

    if (!result) return 0;

    if (PQresultStatus(result) == PGRES_TUPLES_OK && PQntuples(result) > 0) {
        found = 1;
    }

    PQclear(result);
    return found;
}

static void ensure_column(PGconn* conn, const char* table, const char* column, const char* definition) {
    char sql[512];

    if (column_exists(conn, table, column)) {
        return;
    }

    snprintf(sql, sizeof(sql), "ALTER TABLE %s ADD COLUMN %s %s", table, column, definition);
    exec_simple(conn, sql);
}

static void apply_schema_migrations(PGconn* conn) {
    ensure_column(conn, "items", "condition", "TEXT NOT NULL DEFAULT 'Working'");
    ensure_column(conn, "issue_records", "issued_to", "TEXT");
    ensure_column(conn, "issue_records", "issue_date", "TIMESTAMP");
    ensure_column(conn, "issue_records", "return_date", "TIMESTAMP");

    exec_simple(conn,
        "UPDATE items SET condition='Working' WHERE condition IS NULL OR condition='';"
        "UPDATE issue_records SET issued_to=assignee WHERE issued_to IS NULL OR issued_to='';"
        "UPDATE issue_records SET issue_date=created_at WHERE issue_date IS NULL;"
        "UPDATE issue_records SET return_date=actual_return_date "
        "WHERE return_date IS NULL AND actual_return_date IS NOT NULL;");
}

static long query_count(PGconn* conn, const char* stmt_name, const char* sql) {
    PGresult* result = exec_prepared_stmt(conn, stmt_name, sql, 0, NULL);
    long count = 0;

    if (!result) return 0;
    if (PQresultStatus(result) == PGRES_TUPLES_OK && PQntuples(result) == 1) {
        count = atol(result_value(result, 0, 0));
    }
    PQclear(result);
    return count;
}

static int run_process(char* const argv[]) {
    pid_t pid = fork();
    int status = 0;

    if (pid < 0) {
        return 0;
    }

    if (pid == 0) {
        execvp(argv[0], argv);
        _exit(127);
    }

    if (waitpid(pid, &status, 0) < 0) {
        return 0;
    }

    return WIFEXITED(status) && WEXITSTATUS(status) == 0;
}

PGconn* db_init(const char* connection_string) {
    PGconn* conn;

    if (global_connection_string) {
        free(global_connection_string);
        global_connection_string = NULL;
    }

    conn = PQconnectdb(connection_string);
    if (PQstatus(conn) != CONNECTION_OK) {
        fprintf(stderr, "[db] Cannot connect to PostgreSQL: %s\n", PQerrorMessage(conn));
        PQfinish(conn);
        return NULL;
    }

    prepared_statement_count = 0;
    global_connection_string = strdup(connection_string);
    if (!global_connection_string) {
        fprintf(stderr, "[db] Failed to store connection string\n");
        PQfinish(conn);
        return NULL;
    }

    if (!exec_simple(conn,
            "SET statement_timeout = '15s';"
            "SET lock_timeout = '5s';"
            "SET idle_in_transaction_session_timeout = '30s';")) {
        db_close(conn);
        return NULL;
    }

    return conn;
}

void db_close(PGconn* conn) {
    if (conn) {
        PQfinish(conn);
    }
    if (global_connection_string) {
        free(global_connection_string);
        global_connection_string = NULL;
    }
    prepared_statement_count = 0;
}

void db_create_schema(PGconn* conn) {
    if (!exec_simple(conn, SCHEMA_SQL)) {
        return;
    }
    apply_schema_migrations(conn);
}

