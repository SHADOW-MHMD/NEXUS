int db_adjust_stock(PGconn* conn, int item_id, int delta, int user_id,
                    const char* tx_type, double price, const char* notes, const char* ref) {
    char item_id_buf[16];
    char user_id_buf[16];
    char delta_buf[16];
    char new_qty_buf[16];
    char price_buf[32];
    const char* values[2];
    PGresult* result;
    int current_qty;
    int new_qty;
    int ok = 0;

    snprintf(item_id_buf, sizeof(item_id_buf), "%d", item_id);
    snprintf(user_id_buf, sizeof(user_id_buf), "%d", user_id);
    snprintf(delta_buf, sizeof(delta_buf), "%d", delta);
    snprintf(price_buf, sizeof(price_buf), "%.2f", price);

    if (!begin_transaction(conn)) return 0;

    values[0] = item_id_buf;
    result = exec_prepared_stmt(
        conn,
        "db_adjust_stock_lock_item",
        "SELECT quantity FROM items WHERE id = $1::int FOR UPDATE",
        1,
        values);
    if (!result || PQresultStatus(result) != PGRES_TUPLES_OK || PQntuples(result) == 0) {
        if (result) PQclear(result);
        rollback(conn);
        return 0;
    }

    current_qty = result_int(result, 0, 0);
    PQclear(result);
    new_qty = current_qty + delta;
    if (new_qty < 0) {
        rollback(conn);
        return 0;
    }

    snprintf(new_qty_buf, sizeof(new_qty_buf), "%d", new_qty);
    {
        const char* update_values[2] = {new_qty_buf, item_id_buf};
        result = exec_prepared_stmt(
            conn,
            "db_adjust_stock_update_item",
            "UPDATE items SET quantity = $1::int, updated_at = NOW() WHERE id = $2::int",
            2,
            update_values);
        if (!result || PQresultStatus(result) != PGRES_COMMAND_OK) {
            if (result) PQclear(result);
            rollback(conn);
            return 0;
        }
        PQclear(result);
    }

    {
        const char* tx_values[7] = {
            item_id_buf, user_id_buf, tx_type, delta_buf, price_buf,
            notes ? notes : "", ref ? ref : ""
        };
        result = exec_prepared_stmt(
            conn,
            "db_adjust_stock_insert_tx",
            "INSERT INTO transactions (item_id, user_id, type, quantity, unit_price, notes, reference) "
            "VALUES ($1::int, $2::int, $3, $4::int, $5::double precision, $6, $7)",
            7,
            tx_values);
        if (!result || PQresultStatus(result) != PGRES_COMMAND_OK) {
            if (result) PQclear(result);
            rollback(conn);
            return 0;
        }
        PQclear(result);
    }

    ok = commit_transaction(conn);
    if (!ok) rollback(conn);
    return ok;
}

